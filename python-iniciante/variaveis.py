"""
Python é uma linguagem de tipagem dinâmica, ou seja, não é necessário especificar o tipo antes do
nome da variável, como normalmente acontece em outras linguagens; além disso, é possível reatribuir um
valor a uma variável de tipo diferente.
"""

# ex:

a = 2
print(a)

a = 'João'
print(a)

# Função para saber o tipo da variável - type()
b = 'String'
print(type(b))

"""
- O contra-barra (\) anúla o próximo caracter
- Uma variável não pode começar com um número
"""

# tipos:

alpha = 10  # Int
bravo = 'string' # ou "string"
charlie = 10.30 # float
delta = True # Boolean
